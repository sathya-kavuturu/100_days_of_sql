SELECT 
t.request_at,
COUNT(CASE WHEN status IN ('cancelled_by_driver', 'cancelled_by_client') THEN 1 ELSE NULL END ) AS cancelled_trip_count,
COUNT(*)  AS total_trips,
ROUND((100.0 * COUNT(CASE WHEN status IN ('cancelled_by_driver', 'cancelled_by_client') THEN 1 ELSE NULL END ) / COUNT(*)),2)  AS cancelled_percent
FROM Trips t 
JOIN Users c ON t.client_id = c.users_id 
JOIN Users d ON t.driver_id = d.users_id
WHERE c.banned = 'No' AND d.banned = 'No'
GROUP BY request_at


